function loadHead() {

    $.ajax({
        url: '../component/head.html',
        type: 'GET',
        dataType: 'html',
        success: function(data) {
          $('#html').html(data);
        },
        error: function() {
          //alert('Fehler beim Laden der Datei.');
          window.location.href = 'error.html';
        }
      });
}

